function useAuth() {
  const [user, setUser] = useState<any>(null);
  const [loading, setLoading] = useState(true);
  
  useEffect(() => {
    console.log('üîê Auth - Initialisation');
    
    let mounted = true;
    
    supa.auth.getUser().then(r => {
      if (mounted) {
        console.log('üë§ Auth - Utilisateur r√©cup√©r√©:', r.data.user?.email || 'Aucun');
        setUser(r.data.user || null);
        setLoading(false);
      }
    }).catch(err => {
      if (mounted) {
        console.error('‚ùå Auth - Erreur:', err);
        setLoading(false);
      }
    });
    
    const { data: { subscription } } = supa.auth.onAuthStateChange((_e, s) => {
      if (mounted) {
        console.log('üîÑ Auth - Changement d\'√©tat:', s?.user?.email || 'D√©connect√©');
        setUser(s?.user || null);
        setLoading(false);
      }
    });
    
    return () => {
      mounted = false;
      subscription.unsubscribe();
    };
  }, []); // Pas de d√©pendances - une seule fois
  
  return { user, loading };
}

function Login(){
  const [email, setEmail] = useState('operator@example.com');
  const [password, setPassword] = useState('demo-demo');
  const [mode, setMode] = useState<'signin'|'signup'>('signin');
  const [msg, setMsg] = useState<string>('');
  const [submitting, setSubmitting] = useState(false);
  
  console.log('üîë Login - Composant affich√©');
  
  const submit = useCallback(async () => {
    console.log('üìù Login - Tentative de connexion:', email);
    setSubmitting(true);
    setMsg('');
    try {
      if (mode==='signin'){
        const { error } = await supa.auth.signInWithPassword({ email, password });
        if (error) {
          console.error('‚ùå Login - Erreur signin:', error);
          setMsg(`Erreur de connexion: ${error.message}`);
        } else {
          console.log('‚úÖ Login - Connexion r√©ussie');
        }
      } else {
        const { error } = await supa.auth.signUp({ email, password });
        console.log('üìß Login - Inscription:', error ? 'Erreur' : 'Succ√®s');
        setMsg(error ? `Erreur d'inscription: ${error.message}` : 'V√©rifie tes emails pour confirmer.');
      }
    } catch (err) {
      console.error('üí• Login - Erreur inattendue:', err);
      setMsg(`Erreur: ${err instanceof Error ? err.message : 'Erreur inconnue'}`);
    }
    setSubmitting(false);
  }, [email, password, mode]);
  
  return (
    <div className="space-page" style={{display:'grid', placeItems:'center', minHeight:'100vh'}}>
      <div className="card" style={{width:360}}>
        <h2 className="h1">üéÆ Connexion Op√©rateur</h2>
        <div className="col">
          <input 
            className="input" 
            value={email} 
            onChange={e=>setEmail(e.target.value)} 
            placeholder="Email"
            type="email"
            disabled={submitting}
          />
          <input 
            className="input" 
            type="password" 
            value={password} 
            onChange={e=>setPassword(e.target.value)} 
            placeholder="Mot de passe"
            disabled={submitting}
          />
          <div className="row">
            <button 
              onClick={submit} 
              className="primary" 
              disabled={submitting}
              style={{flex: 1}}
            >
              {submitting ? 'Connexion...' : (mode==='signin'?'Se connecter':'Cr√©er un compte')}
            </button>
          </div>
          <div className="row">
            <button 
              onClick={()=>setMode(mode==='signin'?'signup':'signin')}
              disabled={submitting}
              style={{width: '100%'}}
            >
              {mode==='signin'?'Cr√©er un compte':'J\'ai d√©j√† un compte'}
            </button>
          </div>
          {msg && <div className="small" style={{
            color: msg.includes('Erreur') ? '#ff6b6b' : '#4ade80',
            textAlign: 'center',
            marginTop: '8px'
          }}>{msg}</div>}
          <div className="small" style={{marginTop: '12px', color: '#666', textAlign: 'center'}}>
            Comptes de test disponibles
          </div>
        </div>
      </div>
    </div>
  );
}

function App(){
  console.log('üè† App - Composant principal charg√©');
  
  const { user, loading } = useAuth();
  const [org, setOrg] = useState<{ id:string, slug:string, name:string }|null>(null);
  const [matches, setMatches] = useState<MatchInfo[]>([]);
  const [currentPage, setCurrentPage] = useState<'space' | 'match'>('space');
  const [selectedMatch, setSelectedMatch] = useState<MatchInfo | null>(null);
  const [error, setError] = useState<string>('');
  const [dataLoaded, setDataLoaded] = useState(false);

  // V√©rifier la configuration une seule fois
  useEffect(() => {
    const supabaseUrl = import.meta.env.VITE_SUPABASE_URL;
    const supabaseKey = import.meta.env.VITE_SUPABASE_ANON_KEY;
    
    console.log('üîß Config - V√©rification Supabase');
    
    if (!supabaseUrl || !supabaseKey) {
      console.error('‚ùå Config - Variables d\'environnement manquantes');
      setError('Configuration Supabase manquante dans le fichier .env');
    }
  }, []); // Une seule fois

  // Charger les donn√©es utilisateur
  useEffect(() => {
    if (!user?.id || dataLoaded) return;
    
    console.log('üë§ User Effect - Chargement des donn√©es pour:', user.email);
    
    let mounted = true;
    
    async function loadUserData() {
      try {
        console.log('üìä Data - Chargement des organisations...');
        const { data: orgs, error: orgError } = await supa
          .from('org_members_with_org')
          .select('*')
          .eq('user_id', user.id);
        
        if (!mounted) return;
        
        if (orgError) {
          console.error('‚ùå Data - Erreur chargement orgs:', orgError);
          setError(`Erreur de chargement des organisations: ${orgError.message}`);
          return;
        }
        
        console.log('üè¢ Data - Organisations trouv√©es:', orgs?.length || 0);
        
        if (orgs && orgs.length > 0) {
          const userOrg = orgs[0];
          console.log('‚úÖ Data - Organisation s√©lectionn√©e:', userOrg.org_name);
          const orgData = { 
            id: userOrg.org_id, 
            slug: userOrg.org_slug, 
            name: userOrg.org_name || userOrg.name 
          };
          setOrg(orgData);
          
          // Charger les matchs pour cette organisation
          console.log('‚öΩ Matches - Chargement pour org:', orgData.id);
          const { data: matchesData, error: matchError } = await supa
            .from('matches')
            .select('*')
            .eq('org_id', orgData.id)
            .order('scheduled_at');
          
          if (!mounted) return;
          
          if (matchError) {
            console.error('‚ùå Matches - Erreur chargement:', matchError);
            setError(`Erreur de chargement des matchs: ${matchError.message}`);
            return;
          }
          
          console.log('üìã Matches - Trouv√©s:', matchesData?.length || 0);
          setMatches((matchesData as any) || []);
          
        } else {
          console.warn('‚ö†Ô∏è Data - Aucune organisation trouv√©e');
          setError('Aucune organisation trouv√©e pour cet utilisateur');
        }
        
        setDataLoaded(true);
        
      } catch (err) {
        if (!mounted) return;
        console.error('üí• Data - Erreur inattendue:', err);
        setError(`Erreur inattendue: ${err instanceof Error ? err.message : 'Erreur inconnue'}`);
      }
    }
    
    loadUserData();
    
    return () => {
      mounted = false;
    };
  }, [user?.id, dataLoaded]); // D√©pendances sp√©cifiques

  // Reset quand l'utilisateur change
  useEffect(() => {
    if (!user) {
      console.log('üîÑ Reset - Utilisateur d√©connect√©');
      setOrg(null);
      setMatches([]);
      setDataLoaded(false);
      setCurrentPage('space');
      setSelectedMatch(null);
      setError('');
    }
  }, [user?.id]); // Seulement quand l'ID change

  const handleMatchSelect = useCallback((match: MatchInfo) => {
    console.log('üéØ Navigation - S√©lection du match:', match.name);
    console.log('üéØ Navigation - Match ID:', match.id);
    setSelectedMatch(match);
    setCurrentPage('match');
    console.log('üéØ Navigation - Changement de page vers match');
  }, []);

  const handleBackToSpace = useCallback(() => {
    console.log('üîô Navigation - Retour √† l\'espace');
    setCurrentPage('space');
    setSelectedMatch(null);
    console.log('üîô Navigation - Changement de page vers space');
  }, []);

  const handleMatchesUpdate = useCallback((updatedMatches: MatchInfo[]) => {
    console.log('üîÑ Update - Mise √† jour des matchs:', updatedMatches.length);
    setMatches(updatedMatches);
  }, []);

  // M√©moriser les props pour √©viter les re-rendus
  const spacePageProps = useMemo(() => ({
    user,
    org,
    orgs: org ? [org] : [],
    matches,
    onMatchSelect: handleMatchSelect,
    onMatchesUpdate: handleMatchesUpdate
  }), [user, org, matches, handleMatchSelect, handleMatchesUpdate]);

  // Afficher un loader pendant la v√©rification de l'authentification
  if (loading) {
    console.log('‚è≥ Render - Chargement de l\'authentification...');
    return (
      <div className="space-page" style={{display:'grid', placeItems:'center', minHeight:'100vh'}}>
        <div className="card" style={{width:360, textAlign:'center'}}>
          <div className="loading">
            <div>‚è≥ Chargement de l'Operator...</div>
            <div className="small" style={{marginTop:'10px', color:'#666'}}>
              V√©rification de l'authentification...
            </div>
          </div>
        </div>
      </div>
    );
  }

  // Afficher les erreurs
  if (error) {
    console.log('‚ùå Render - Affichage de l\'erreur:', error);
    return (
      <div className="space-page" style={{display:'grid', placeItems:'center', minHeight:'100vh'}}>
        <div className="card" style={{width:400, textAlign:'center'}}>
          <h2 className="h1" style={{color:'#ff6b6b'}}>‚ö†Ô∏è Erreur</h2>
          <div style={{color:'#ff6b6b', marginBottom:'16px'}}>{error}</div>
          <button onClick={() => window.location.reload()} className="primary">
            üîÑ Recharger la page
          </button>
          <div className="small" style={{marginTop:'16px', color:'#666'}}>
            V√©rifiez la console (F12) pour plus de d√©tails
          </div>
        </div>
      </div>
    );
  }

  // Afficher la page de connexion si pas d'utilisateur
  if (!user) {
    console.log('üîê Render - Affichage de la page de connexion');
    return <Login />;
  }

  console.log('‚úÖ Render - Affichage de l\'interface principale');
  console.log('üìä State - √âtat actuel:', { 
    user: user?.email, 
    org: org?.name, 
    matchesCount: matches.length, 
    currentPage 
  });

  // Navigation entre les pages
  if (currentPage === 'match' && selectedMatch) {
    console.log('üéÆ Render - Page de match');
    return (
      <MatchPage 
        match={selectedMatch} 
        onBack={handleBackToSpace}
      />
    );
  }

  console.log('üè† Render - Page d\'espace');
  return <SpacePage {...spacePageProps} />;
}

console.log('üéØ Main - Initialisation du root React');

